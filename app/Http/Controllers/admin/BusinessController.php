    public function changeAdminStatus($id)
    {
        $business = Business::find($id);
        if($business->admin_status == 'passive'){
            $business->admin_status = 'active';
        }
        else{
            $business->admin_status = 'passive';
        }
        $business->save();
        return back();
    }

    public function adminBusinessDestroy($id)
    {
        $business = Business::find($id);
        $business->delete();
        return back();
    }
